#!/usr/bin/env node
/**
 * Creates .env.local from pasted Firebase web config.
 * Usage: node scripts/create-env-local.mjs
 * (interactive prompts)
 */
import fs from 'fs';
import path from 'path';
import readline from 'readline';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const envPath = path.join(__dirname, '..', '.env.local');

const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
const ask = (q) => new Promise((r) => rl.question(q, r));

async function main() {
  console.log('\nPodium — Firebase .env.local setup\n');
  console.log('Firebase Console → Project settings → Your apps → Web app → firebaseConfig\n');

  const apiKey = (await ask('VITE_FIREBASE_API_KEY: ')).trim();
  const authDomain = (await ask('VITE_FIREBASE_AUTH_DOMAIN: ')).trim();
  const projectId = (await ask('VITE_FIREBASE_PROJECT_ID: ')).trim();
  const storageBucket = (await ask('VITE_FIREBASE_STORAGE_BUCKET: ')).trim();
  const messagingSenderId = (await ask('VITE_FIREBASE_MESSAGING_SENDER_ID: ')).trim();
  const appId = (await ask('VITE_FIREBASE_APP_ID: ')).trim();
  const adminEmails = (await ask('VITE_ADMIN_EMAILS (your Google email): ')).trim();

  if (!apiKey || !projectId) {
    console.error('apiKey and projectId are required.');
    process.exit(1);
  }

  const content = `# Generated by scripts/create-env-local.mjs
VITE_FIREBASE_API_KEY=${apiKey}
VITE_FIREBASE_AUTH_DOMAIN=${authDomain}
VITE_FIREBASE_PROJECT_ID=${projectId}
VITE_FIREBASE_STORAGE_BUCKET=${storageBucket}
VITE_FIREBASE_MESSAGING_SENDER_ID=${messagingSenderId}
VITE_FIREBASE_APP_ID=${appId}
VITE_ADMIN_EMAILS=${adminEmails}
`;

  fs.writeFileSync(envPath, content);
  console.log(`\nWrote ${envPath}`);
  console.log('Run: npm run dev\n');
  rl.close();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
